using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _14_WhileLoops
{
    class Program
    {
        static void Main(string[] args)
        {
            int index = 1;
            while (index <= 5) //executes the code until the conditions are met
            {
                Console.WriteLine(index);
                index++;
            }



            Console.ReadLine();
        }
    }
}
